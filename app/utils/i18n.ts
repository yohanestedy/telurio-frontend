export const messages: Record<AppLanguage, TranslationTable> = { id, en };

export function getCurrentLanguage(): AppLanguage {
  if (typeof window === "undefined") {
    return "id";
  }

  const storedLanguage = window.localStorage.getItem("telurio.language");
  return storedLanguage === "en" ? "en" : "id";
}

export function translateMessage(language: AppLanguage, key: string) {
  return messages[language][key] ?? messages.id[key] ?? key;
}

export function roleText(language: AppLanguage, role: Role) {
  return translateMessage(language, `role.${role}`);
}
